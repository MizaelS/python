# Declaración de variables
numero1 = 2
numero2 = 6

# Comparación de los números
if numero1 == numero2:
    print("Los dos números son iguales")
else:
    if numero1 > numero2:
        print("El número 1 es mayor al número 2")
    else:
        print("El número 1 es menor al número 2")
